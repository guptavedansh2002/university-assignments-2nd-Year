print("Grades : O, A, B, C, F")
grade = input("Enter your grade : ")
if grade == "O":
    print("Outstanding")
elif grade == "A":
    print("Very Good")
elif grade == "B":
    print("Good")
elif grade == "C":
    print("Average")
elif grade == "F":
    print("Fail")
else:
    print("Invalid Input!!")
